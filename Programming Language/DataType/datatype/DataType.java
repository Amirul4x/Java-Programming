package datatype;                      // package declaration

public class DataType {              // class declaration
    public static void main(String[] args) {                   // main method
        boolean b=true; char c='A'; short s=100; int i=1000;  // variable declaration and initialization
        long l=10000L; float f=10.5f; double d=20.5;         // variable declaration and initialization
        System.out.println("boolean b= " + b);
        System.out.println("char c= " + c);
        System.out.println("short s= " + s);   
        System.out.println("int i= " + i);  
        System.out.println("long l= " + l);
        System.out.println("float f= " + f);
        System.out.println("double d= " + d);
    }
}